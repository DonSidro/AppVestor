package com.calldorado.appvestor.data.models;


/**
 * Object class for items in graph
 */
public class GraphItems {

    private String dat0;
    private String investment_id;
    private String amount;

    public GraphItems() {
    }

    public String getDat0() {
        return dat0;
    }

    public void setDat0(String dat0) {
        this.dat0 = dat0;
    }

    public String getInvestment_id() {
        return investment_id;
    }

    public void setInvestment_id(String investment_id) {
        this.investment_id = investment_id;
    }

    public String getAmount() {
        return amount;
    }

    public void setAmount(String amount) {
        this.amount = amount;
    }
}
